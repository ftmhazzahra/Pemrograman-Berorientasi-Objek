    private void bt_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_cariActionPerformed
        // TODO add your handling code here:
        try {
            java.sql.Connection conn = k.getCon();
            String sql = "select * from tbdata where no=? or nama=?";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, text_cari.getText());
            pst.setString(2, text_cari.getText());
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                text_no.setText(rs.getString("no"));
                text_nama.setText(rs.getString("nama"));
                text_alamat.setText(rs.getString("alamat"));
                text_hp.setText(rs.getString("hp"));
                JOptionPane.showMessageDialog(null, "Data: " + text_cari.getText() + " ditemukan, Apakah Anda Ingin Mengedit atau Menghapus?");
            } else {
                JOptionPane.showMessageDialog(null, "Data: " + text_cari.getText() + " Tidak ditemukan.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        text_cari.requestFocus();
    }//GEN-LAST:event_bt_cariActionPerformed

