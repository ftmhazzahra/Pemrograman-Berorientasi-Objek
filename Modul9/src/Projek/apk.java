    public void refreshTable(){
        model = new DefaultTableModel();
        model.addColumn("NO");
        model.addColumn("Nama");
        model.addColumn("Alamat");
        model.addColumn("Telepon/HP");
        tabel_apk.setModel(model);
        try {
            this.stat = k.getCon().prepareStatement("select * from tbdata");
            this.rs = this.stat.executeQuery(); //mengabil data dari tabel GUI
            while (rs.next()) {                
                Object[] data = {
                    rs.getInt("no"),
                    rs.getString("nama"),
                    rs.getString("alamat"),
                    rs.getInt("hp")
                };
                model.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        text_no.setText("");
        text_nama.setText("");
        text_alamat.setText("");
        text_hp.setText("");
    }

